// Напишите код, который посчитает сумму всех парных элементов в массиве.
// Использовать встроенные методы массивов — нельзя.

////////////////// Задание //////////////////
// const array = [1, 2, 3, 4];

////////////////// Решение //////////////////

const array = [1, 2, 3, 4, 5, 6, 7, 8];
let sum = 0;

for (let i = 0; i <= array.length; i++) {
    let el = array[i];
    if (el % 2 === 0) {
        sum += el;
        console.log(sum);
    }
}
